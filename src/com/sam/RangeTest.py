# Range can be used to specify the range of numbers etc.
# If you want to run a for loop for 10 times you could use the following:
# by default the range starts from 0
for i in range(10):
    print(i)

# you can specify a start and end for range
for i in range(10, 20):
    print(i)

# you can set increment value in the range as well as the third argument
for i in range(30, 50, 2):
    print(i)
